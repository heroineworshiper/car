void* serial_thread(void *ptr)
{
	unsigned char buffer[256];
	int counter = 0;
	int state = 0;
	while(1)
	{
		unsigned char c;
		if(read(serial_fd, &c, 1) < 0)
		{
			printf("serial_thread %d: Unplugged\n", __LINE__);
			exit(1);
		}
		
		switch(state)
		{
			case 0:
				if(c == 0xff)
				{
					state++;
				}
				else
				{
					printf("%c", c);
					fflush(stdout);
				}
				break;
			case 1:
				if(c == SYNC_CODE)
				{
					state++;
					counter = 0;
				}
				else
				{
					state = 0;
				}
				break;
			case 2:
				buffer[counter++] = c;
				if(counter >= 6 * 4)
				{
					pthread_mutex_lock(&serial_lock);
					int offset = 0;
					current_heading = READ_FLOAT32(buffer, offset);
					target_heading = READ_FLOAT32(buffer, offset);
					p_result = READ_FLOAT32(buffer, offset);
					i_result = READ_FLOAT32(buffer, offset);
					d_result = READ_FLOAT32(buffer, offset);
					feedback = READ_FLOAT32(buffer, offset);
					pthread_mutex_unlock(&serial_lock);
					state = 0;
				}
				break;
		}
	
	}
}

