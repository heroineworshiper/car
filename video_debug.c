// these force 4 byte alignment
float read_float32(const unsigned char *buffer)
{
	unsigned char temp[4];
	temp[0] = buffer[0];
	temp[1] = buffer[1];
	temp[2] = buffer[2];
	temp[3] = buffer[3];
	return *(float*)temp;
}

#define READ_FLOAT32(buffer, offset) \
({ \
	offset += 4; \
	read_float32(buffer + offset - 4); \
})

void draw_char_row(int x, int y, unsigned char *src)
{
	int i;
	unsigned char *in_y2 = in_y + y * OUTPUT_W + x;
	unsigned char *in_u2 = in_u + y * OUTPUT_W + x;
	unsigned char *in_v2 = in_v + y * OUTPUT_W + x;
	
	for(i = 0; i < CHAR_W; i++)
	{
		*in_y2 = *src;
		*in_u2 = 0x80;
		*in_v2 = 0x80;
		in_y2 -= OUTPUT_W;
		in_u2 -= OUTPUT_W;
		in_v2 -= OUTPUT_W;
		src++;
	}
}

void draw_text(int x, int y, char *text)
{
	int i, j;
	for(i = 0; i < strlen(text); i++)
	{
		char c = text[i];
// location in the font bitmap
		int in_x = 388;
		int in_y = 0;
		if(c >= '0' && c <= '9')
		{
			in_x = CHAR_W * 2 * (c - '0');
			in_y = CHAR_H * 3;
		}
		else
		if(c == '-')
		{
			in_x = 0;
			in_y = CHAR_H * 2;
		}
		else
		if(c == '.')
		{
			in_x = CHAR_W * 2;
			in_y = CHAR_H * 2;
		}
		else
		if(c >= 'A' && c <= 'R')
		{
			in_x = CHAR_W * 2 * (c - 'A');
			in_y = 0;
		}
		else
		if(c >= 'S' && c <= 'Z')
		{
			in_x = CHAR_W * 2 * (c - 'S');
			in_y = CHAR_H * 1;
		}
		

// draw rotated character rows
		for(j = 0; j < CHAR_H2 && j + x < OUTPUT_W; j ++)
		{
			draw_char_row(x + j, 
				y, 
				font_luma + font_width * (in_y + j) + in_x);
		}
		y -= CHAR_W;
	}
}

